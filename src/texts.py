"""Тексты бота (HTML-форматирование, без эмодзи в копирайте).

Всё, что видит пользователь, собрано здесь — чтобы копирайт правился в одном месте,
а не растекался по хендлерам. Аудитория — «синие воротнички» 35–50 лет: формулировки
простые, без канцелярита и жаргона.
"""
from __future__ import annotations

# ── Названия кнопок главного меню (используются и в клавиатуре, и в фильтрах) ──
BTN_ASK = "Задать вопрос"
BTN_EMPLOYER = "Проверить работодателя"
BTN_BALANCE = "Баланс и оплата"
BTN_HELP = "Помощь"
BTN_MAIN_MENU = "Главное меню"

# Контакт поддержки (уточняется у заказчика; правится в одном месте).
SUPPORT_CONTACT = "@URIST2026_support"


def welcome_new(first_name: str | None, free_requests: int) -> str:
    """Приветствие для нового пользователя: кто мы, дисклеймер, бесплатные запросы."""
    name = (first_name or "").strip()
    hello = f"Здравствуйте, {name}." if name else "Здравствуйте."
    return (
        f"<b>{hello}</b>\n\n"
        "Это <b>юридический помощник</b> для тех, кто работает и ищет работу: "
        "охранников, водителей, монтажников и других. Помогаю разобраться в "
        "бытовых рабочих вопросах простым языком:\n"
        "· как проверить работодателя перед устройством;\n"
        "· что делать, если задерживают или не платят зарплату;\n"
        "· законны ли штрафы на работе и как себя защитить.\n\n"
        f"В подарок на старте — <b>{free_requests} бесплатных запросов</b>. "
        "Один запрос — это один ответ по существу со ссылками на источники.\n\n"
        f"{DISCLAIMER}\n\n"
        "Выберите, с чего начать."
    )


def welcome_back(first_name: str | None, balance: int) -> str:
    """Короткое приветствие для вернувшегося пользователя."""
    name = (first_name or "").strip()
    hello = f"С возвращением, {name}." if name else "С возвращением."
    return (
        f"<b>{hello}</b>\n\n"
        f"На вашем счету запросов: <b>{balance}</b>.\n"
        "Выберите, что вас интересует."
    )


# Юридический дисклеймер — показывается в приветствии и в «Помощи».
DISCLAIMER = (
    "<i>Важно: бот даёт справочную информацию и не заменяет очную консультацию "
    "юриста. Для официальных действий проверяйте актуальные документы или "
    "обращайтесь к специалисту.</i>"
)


