def _fmt_price(amount) -> str:
    """Цена без лишних нулей: 199 → «199», 199.50 → «199.50»."""
    s = f"{amount:.2f}".rstrip("0").rstrip(".")
    return s or "0"


_PAY_STATUS_RU = {
    "succeeded": "оплачен",
    "pending": "ожидает оплаты",
    "canceled": "отменён",
}


def _history_block(history: list) -> str:
    """Компактная история последних покупок (записи payments)."""
    if not history:
        return ""
    lines = []
    for p in history:
        d = p["created_at"].strftime("%d.%m.%Y")
        st = _PAY_STATUS_RU.get(p["status"], p["status"])
        lines.append(f"· {d} — пакет {p['package']} за {_fmt_price(p['amount'])} ₽ ({st})")
    return "\n\n<b>Ваши покупки:</b>\n" + "\n".join(lines)


def balance_screen(balance: int, prices: dict, history: list) -> str:
    """Экран «Баланс и оплата»: остаток, цены пакетов, история покупок."""
    if prices:
        price_lines = "\n".join(
            f"· <b>{pkg}</b> запросов — {_fmt_price(prices[pkg])} ₽"
            for pkg in sorted(prices)
        )
        buy = (
            "Можно докупить пакет запросов:\n"
            f"{price_lines}\n\n"
            "Выберите пакет кнопкой ниже."
        )
    else:
        buy = "<i>Пакеты временно недоступны. Загляните чуть позже.</i>"
    return (
        "<b>Баланс и оплата</b>\n\n"
        f"На вашем счету запросов: <b>{balance}</b>.\n\n"
        f"{buy}"
        f"{_history_block(history)}"
    )


