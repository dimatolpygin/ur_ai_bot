def answer_footer(balance: int) -> str:
    """Приписка под ответом ИИ: остаток и подсказка про новый диалог."""
    if balance <= 0:
        return (
            "\n\n<i>Это был последний доступный запрос. Пополнить можно в разделе "
            "«Баланс и оплата».</i>"
        )
    return (
        f"\n\n<i>Осталось запросов: <b>{balance}</b>. Можно задать следующий вопрос — "
        "я помню наш разговор. «Новый диалог» — начать тему с чистого листа.</i>"
    )


def sources_block(urls: list[str], limit: int = 5) -> str:
    """Компактный список источников под ответом (когда был веб-поиск)."""
    if not urls:
        return ""
    lines = [f"{i}. {u}" for i, u in enumerate(urls[:limit], 1)]
    return "\n\n<b>Источники:</b>\n" + "\n".join(lines)


