    hello = f"Здравствуйте, {name}." if name else "Здравствуйте."
    return (
        f"<b>{hello}</b>\n\n"
        "Это <b>юридический помощник</b> для тех, кто работает и ищет работу: "
        "охранников, водителей, монтажников и других. Помогаю разобраться в "
        "бытовых рабочих вопросах простым языком:\n"
        "· как проверить работодателя перед устройством;\n"
        "· что делать, если задерживают или не платят зарплату;\n"
        "· законны ли штрафы на работе и как себя защитить.\n\n"
        f"В подарок на старте — <b>{free_requests} бесплатных запросов</b>. "
        "Один запрос — это один ответ по существу со ссылками на источники.\n\n"
        f"{DISCLAIMER}\n\n"
        "Выберите, с чего начать."
    )


